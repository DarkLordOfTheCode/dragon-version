_default = [
    "  _____  ",
    " /     \\ ",
    "| ? ? ? |",
    " \\_____/ ",
    "  |   |  ",
]


def get_sprite(name):
    return list(_sprites.get(name, _default))


def hp_bar(current, maximum, width=8):
    if maximum == 0:
        return "[" + "░" * width + "]"
    filled = max(0, min(width, round((current / maximum) * width)))
    return "[" + "█" * filled + "░" * (width - filled) + "]"


def show_battle_screen(player_mon, enemy_mon):
    p_lines = get_sprite(player_mon["name"])
    e_lines = get_sprite(enemy_mon["name"])

    height = max(len(p_lines), len(e_lines))
    while len(p_lines) < height:
        p_lines.append("")
    while len(e_lines) < height:
        e_lines.append("")

    p_width = max((len(l) for l in p_lines), default=9)
    e_width = max((len(l) for l in e_lines), default=9)
    gap = "       "

    print()
    for p_line, e_line in zip(p_lines, e_lines):
        print(p_line.ljust(p_width) + gap + e_line.ljust(e_width))

    p_bar = hp_bar(player_mon["hp"], player_mon["max_hp"])
    e_bar = hp_bar(enemy_mon["hp"], enemy_mon["max_hp"])
    col_width = p_width + len(gap)
    p_label = f"  {player_mon['name']} Lv.{player_mon['level']}  {p_bar} {player_mon['hp']}/{player_mon['max_hp']}"
    e_label = f"  {enemy_mon['name']} Lv.{enemy_mon['level']}  {e_bar} {enemy_mon['hp']}/{enemy_mon['max_hp']}"
    print(p_label.ljust(col_width) + e_label)
    print()
